nums = [48, -10, 9, 38, 17, 50, -5, 43, 46, 12]
print(nums[:5]) #первые 5 элементов
print(nums[:-2]) #все, кроме последних двух
print(nums[::2]) #все с чётными индексами
print(nums[1::2]) #все с нечетными индексами
print(nums[::-1]) #все в обратном порядке
print(nums[-1::-2]) #все в обратном порядке через один, начиная с последнего